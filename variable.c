//1.Declare an integer variable and print it
#include <stdio.h>
int main() {
    int a = 10;   
    printf("%d", a);
    return 0;
}

//2.Declare two integer variables and print them
#include <stdio.h>
int main() {
    int a = 5, b = 15; 
    printf("%d %d", a,b);
    return 0;
}

//3.Declare a float variable and print it
#include <stdio.h>
int main() {
    float x = 2.5;   
    printf("%.2f",x);
    return 0;
}

//4.Declare a character variable and print it
#include <stdio.h>
int main() {
    char ch = 'X';   
    printf("%c", ch);
    return 0;
}

//5.Change variable value and print updated value
#include <stdio.h>
int main() {
    int a = 10;
    a = 20;   
    printf("%d", a);
    return 0;
}

//6.Add two variables
#include <stdio.h>
